export function useI18n() {
  function setLocale(l: Locale) {
    locale.value = l
    localStorage.setItem('chatllm-locale', l)
  }

  function initLocale() {
    const stored = localStorage.getItem('chatllm-locale') as Locale | null
    if (stored) {
      locale.value = stored
    } else {
      locale.value = navigator.language.startsWith('zh') ? 'zh' : 'en'
    }
  }

  const t = computed(() => {
    const dict = messages[locale.value]
    return (key: string) => dict[key] || key
  })

  return { locale, setLocale, initLocale, t }
}
